#!/usr/bin/env node
// Run: npm run gen:temple

const fs   = require('fs');
const path = require('path');

const TOTAL_FRAMES = 16;
const MAX_AMP      = 2;   // max 2-char shift at stalk tips
const LEFT_END     = 60;  // cols 0-59 = left bamboo region
const RIGHT_START  = 140; // cols 140-199 = right bamboo region

// Parse the single static frame from frames.js
const src = fs.readFileSync(
  path.join(__dirname, '../app/data/animation/frames.js'), 'utf8'
);
const firstTick = src.indexOf('`') + 1;
const lastTick  = src.lastIndexOf('`');
const raw       = src.slice(firstTick, lastTick);

// Strip leading/trailing blank lines, keep content rows
const allRows = raw.split('\n');
let topBlank = 0;
while (topBlank < allRows.length && allRows[topBlank].trim() === '') topBlank++;
let botBlank = allRows.length - 1;
while (botBlank >= 0 && allRows[botBlank].trim() === '') botBlank--;
const contentRows = allRows.slice(topBlank, botBlank + 1);

// Pad every row to uniform width (200) for consistent column indexing
const FRAME_WIDTH = 200;
const rows = contentRows.map(r => r.padEnd(FRAME_WIDTH, ' '));
const ROW_COUNT = rows.length; // ~108

// Shift a string segment left (negative) or right (positive) by amt characters
function shiftSeg(seg, amt) {
  if (amt === 0) return seg;
  const len = seg.length;
  if (amt > 0) {
    return ' '.repeat(Math.min(amt, len)) + seg.slice(0, Math.max(0, len - amt));
  } else {
    const s = Math.min(-amt, len);
    return seg.slice(s) + ' '.repeat(s);
  }
}

const frames = [];

for (let f = 0; f < TOTAL_FRAMES; f++) {
  // Sine sway: -1 to +1 across the loop
  const sway = Math.sin((f / TOTAL_FRAMES) * Math.PI * 2);

  const animated = rows.map((row, ri) => {
    // Weight: rows near the top sway more (tips), rows near the bottom stay fixed (roots)
    const frac   = 1 - ri / Math.max(1, ROW_COUNT - 1); // 1.0 at top, 0.0 at bottom
    const weight = frac * frac * frac;                   // cubic — very subtle near base
    const shift  = Math.round(sway * MAX_AMP * weight);

    if (shift === 0) return row.trimEnd();

    const left   = row.slice(0, LEFT_END);
    const center = row.slice(LEFT_END, RIGHT_START);
    const right  = row.slice(RIGHT_START);

    return (shiftSeg(left, shift) + center + shiftSeg(right, shift)).trimEnd();
  });

  frames.push(animated.join('\n'));
}

const output = [
  '// Auto-generated by scripts/generate-temple-frames.js — do not edit by hand',
  `// ${TOTAL_FRAMES} frames — temple + bamboo scene`,
  `export const frames = ${JSON.stringify(frames, null, 2)};`,
].join('\n') + '\n';

const outPath = path.join(__dirname, '../app/data/bambooFrames.js');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, output, 'utf8');
console.log(`✓ Generated ${frames.length} frames → ${outPath} (${(output.length / 1024).toFixed(1)} KB)`);
console.log(`  Frame size: ${ROW_COUNT} rows × ${FRAME_WIDTH} cols`);
